    /**
     * Get system language fallback
     */
    window.getSystemLanguage = function () {
        const lang = navigator.language || navigator.userLanguage || 'en';
        return lang.startsWith('zh') ? 'zh' : 'en';
    };

    /**
     * Current language resolver
     */
    window.getCurrentLang = function () {
        let lang = state.uiLanguage || 'auto';
        if (lang === 'auto') {
            lang = window.getSystemLanguage();
        }
        return lang === 'chinese' ? 'zh' : (lang === 'english' ? 'en' : lang);
    };

    /**
     * Translation helper
     */
    /**
     * Translate a key with optional parameter replacement
     * Usage: window.t('log_task_finished', { duration: 5 })
     */
    window.t = function (key, params = null) {
        const lang = window.getCurrentLang();
        const dict = translations[lang] || translations['en'];
        let text = dict[key] || key;

        if (params && typeof params === 'object') {
            Object.keys(params).forEach(p => {
                const val = params[p];
                if (val !== undefined && val !== null) {
                    text = text.replace(new RegExp(`\\{${p}\\}`, 'g'), val);
                }
            });
        }
        return text;
    };

